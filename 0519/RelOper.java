// 관계연산자

public class RelOper {
	public static void main(String[] args) {
		System.out.println(55 == 55); // == 왼쪽의 값과 오른쪽의 값이 같냐고 물어보는 연산자.
		System.out.println(55 != 55); // ! 왼쪽의 값과 오른쪽의 값이 다르냐 물어보는 연산자.
		System.out.println(55 > 55);  // > 왼쪽의 값보다 오른쪽의 값이 더 작냐 물어보는 연산자.
		System.out.println(55 < 55);  // < 왼쪽의 값보다 오른쪽의 값이 더 크냐 물어보는 연산자.
		System.out.println(55 >= 55); // >= 왼쪽의 값이 오른쪽의 값보다 크거나 같으냐 물어보는 연산자.
		System.out.println(55 <= 55); // <= 왼쪽의 값이 오른쪽의 값보다 작거나 같으냐 물어보는 연산자.
	}
}